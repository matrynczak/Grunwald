    private String wyciagnijDate(){
        String data = "";
        String rok = poleRok.getText();
        String miesiac = comboMiesiac.getSelectedItem().toString();
//        Integer miesiacLiczba = Integer.parseInt(miesiac);
//        String miesiacStr  = "";
//        if(miesiacLiczba < 10)  miesiacStr = "0" + miesiac.toString();
        Integer dzienLiczba = Integer.parseInt(poleDzien.getText());
        String dzienStr = poleDzien.getText();
        if(dzienLiczba < 10) dzienStr = "0" + dzienLiczba.toString();
        
        data += poleRok.getText() + "-";
        data += miesiac + "-";
        data += dzienStr;
               
        return data;
    }
    
