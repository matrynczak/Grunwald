    private void guzikZmniejszActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guzikZmniejszActionPerformed
        Integer dzien = Integer.parseInt(poleDzien.getText());
        Integer rok = Integer.parseInt(poleRok.getText());
        int miesiac = comboMiesiac.getSelectedIndex();
        
        if(radioDni.isSelected()){
            if(check30.isSelected())
                if(dzien == 1)
                    poleDzien.setText("30");
                else {dzien -= 1;
                    poleDzien.setText(dzien.toString());
                }
            else{
                    if(dzien == 1)
                        poleDzien.setText("31");
                        
                    else {dzien -= 1;
                        poleDzien.setText(dzien.toString());
                    }
                } 
        }
        
        if(radioLata.isSelected())
            rok -= 1;

        poleRok.setText(rok.toString());
    }//GEN-LAST:event_guzikZmniejszActionPerformed

